#!/usr/bin/env bash
# Единственная точка входа: воспроизводит ML-challenger — диагностику
# систематических ошибок структурной модели районов (CCR/Гамильтона-Перри)
# градиентным бустингом на признаках, которые CCR игнорирует.
# Прогон детерминирован (сеяный Монте-Карло: LOO-по-областям CV,
# перестановочный нуль, повторная CV) -> байт-в-байт те же числа.
# Требования: Python >= 3.10, только стандартная библиотека. Время: ~5 минут.
set -euo pipefail
cd "$(dirname "$0")/.."
export PYTHONDONTWRITEBYTECODE=1
PY="${PYTHON:-python3}"

echo "== 1/3 Панель + CCR-остаток + бустинг + перестановочный нуль (~5 мин) =="
"$PY" -m etl.challenger

echo "== 2/3 Инварианты =="
"$PY" checks/tests/test_invariants.py

echo "== 3/3 Сверка с заявленными результатами =="
"$PY" code/verify.py

echo "ГОТОВО: ML-challenger воспроизведён и совпадает с заявленным."
