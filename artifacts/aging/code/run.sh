#!/usr/bin/env bash
# Единственная точка входа: воспроизводит все индикаторы старения и
# контрфактную передвижку, проверяет инварианты и сверяет с заявленными
# результатами. Требования: Python >= 3.10, только стандартная библиотека.
set -euo pipefail
cd "$(dirname "$0")/.."
export PYTHONDONTWRITEBYTECODE=1
PY="${PYTHON:-python3}"

echo "== 1/3 Расчёт индикаторов и контрфактной передвижки =="
"$PY" -m etl.aging

echo "== 2/3 Инварианты =="
"$PY" checks/tests/test_invariants.py

echo "== 3/3 Сверка с заявленными результатами =="
"$PY" code/verify.py

echo "ГОТОВО: индикаторы старения воспроизведены и совпадают с заявленными."
