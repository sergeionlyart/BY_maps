#!/usr/bin/env bash
# Единственная точка входа: воспроизводит дифференциалы, классификацию 3x3
# и все регрессии из сырых ответов API Белстата, проверяет инварианты и
# сверяет с заявленным. Требования: Python >= 3.10, стандартная библиотека.
set -euo pipefail
cd "$(dirname "$0")/.."
export PYTHONDONTWRITEBYTECODE=1
PY="${PYTHON:-python3}"

echo "== 1/3 Расчёт дифференциалов, классификации и регрессий =="
"$PY" -m etl.wages

echo "== 2/3 Инварианты =="
"$PY" checks/tests/test_invariants.py

echo "== 3/3 Сверка с заявленными результатами =="
"$PY" code/verify.py

echo "ГОТОВО: INF-03 воспроизведён и совпадает с заявленным."
