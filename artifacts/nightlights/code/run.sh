#!/usr/bin/env bash
# Единственная точка входа: воспроизводит гармонизацию 1992-2024 (схема
# «мост»), оценку эластичностей, модель светимости 2030-2075 и финальный
# набор nightlights_v2.json из завендоренных зональных сумм - на
# стандартной библиотеке (без rasterio). Затем инварианты и сверка с
# заявленными результатами. Требования: Python >= 3.10, stdlib.
#
# Растровые шаги (вырезки -> зональные CSV -> PNG-кадры -> рилс) требуют
# rasterio/numpy/Pillow и здесь НЕ вызываются; их код и вырезки растров
# вендорены, порядок описан в AGENT.md.
set -euo pipefail
cd "$(dirname "$0")/.."
export PYTHONDONTWRITEBYTECODE=1
PY="${PYTHON:-python3}"

echo "== 1/5 Гармонизация DMSP/VIIRS: мост, стык, спайк-отчёт =="
"$PY" -m etl.nightlights_harmonize

echo "== 2/5 Эластичности: межрайонные (источник beta) и панель =="
"$PY" -m etl.nightlights_model

echo "== 3/5 Финальный набор: ряд 1992-2024 + модель 2030-2075 =="
"$PY" -m etl.nightlights_v2

echo "== 4/5 Инварианты =="
"$PY" checks/tests/test_invariants.py

echo "== 5/5 Сверка с заявленными результатами =="
"$PY" code/verify.py

echo "ГОТОВО: INF-08 v2 воспроизведён и совпадает с заявленным."
