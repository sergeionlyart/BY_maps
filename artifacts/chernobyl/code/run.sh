#!/usr/bin/env bash
# Единственная точка входа: воспроизводит реестр зон, матчинг контролей
# и все числа исследования, проверяет инварианты и сверяет с заявленными.
# Требования: Python >= 3.10, только стандартная библиотека.
set -euo pipefail
cd "$(dirname "$0")/.."
export PYTHONDONTWRITEBYTECODE=1
PY="${PYTHON:-python3}"

echo "== 1/3 Реестр зон, классификация, матчинг контролей =="
"$PY" -m etl.chernobyl

echo "== 2/3 Инварианты =="
"$PY" checks/tests/test_invariants.py

echo "== 3/3 Сверка с заявленными результатами =="
"$PY" code/verify.py

echo "ГОТОВО: чернобыльский след воспроизведён и совпадает с заявленным."
