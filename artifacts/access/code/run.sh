#!/usr/bin/env bash
# Единственная точка входа: воспроизводит травел-таймы, пояса, профиль
# и регрессию из завендоренного дорожного графа и реестра переходов,
# проверяет инварианты и сверяет с заявленным.
# Требования: Python >= 3.10, стандартная библиотека (~1-2 мин).
set -euo pipefail
cd "$(dirname "$0")/.."
export PYTHONDONTWRITEBYTECODE=1
PY="${PYTHON:-python3}"

echo "== 1/3 Травел-таймы (Дейкстра), пояса, профиль, регрессия =="
"$PY" -m etl.access

echo "== 2/3 Инварианты =="
"$PY" checks/tests/test_invariants.py

echo "== 3/3 Сверка с заявленными результатами =="
"$PY" code/verify.py

echo "ГОТОВО: INF-04 воспроизведён и совпадает с заявленным."
